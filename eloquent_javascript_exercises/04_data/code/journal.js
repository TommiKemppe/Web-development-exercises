function range(start, end, step){
  let rangeArray = [];
  if (step == undefined){
    step = 1;
  }
  if(step > 0){
    for (let i = start; i <= end; i += step){
      rangeArray.push(i);
    }
  }
  else if(step < 0){
    for (let i = start; i >= end; i += step){
      rangeArray.push(i);
    }
  }
  else{
    return -1;
  }
  return rangeArray;
}
console.log(range(9, 2, -1));

function sum(numbers){
  let sum = 0;
  for(let number of numbers){
    sum +=number;
  }
  return sum;
}
